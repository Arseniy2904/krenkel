package Urok_02;

public class Zadanie1_9 {
    public static void main(String[] ags) {
        int a = 4;
        int b = 3;
        int sum = (a * a) + (b * b);
        int difference = (a * a) - (b * b);
        int work = (a * a) * (b * b);
        double quotient = (a * a) / (b * b);
        System.out.println("Сумма квадратов (а и b) = " + sum);
        System.out.println("Разность квадратов (а и b) = " + difference);
        System.out.println("Произведение квадратов (а и b) = " + work);
        System.out.println("Частное квадратов (a и b) = " + quotient);
    }
}
